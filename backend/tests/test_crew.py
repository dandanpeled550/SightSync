def _add_member(client, project_id: int, **kwargs) -> dict:
    """POST a crew member and return the parsed JSON body."""
    payload = {"name": "Alice", **kwargs}
    resp = client.post(f"/projects/{project_id}/crew", json=payload)
    assert resp.status_code == 201, resp.text
    return resp.json()


