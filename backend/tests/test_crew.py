class TestListCrew:
    def test_empty_project_returns_empty_list(self, seeded_client):
        """GET /projects/{id}/crew on a project with no crew returns []."""
        resp = seeded_client.get(f"/projects/{seeded_client.project_id}/crew")
        assert resp.status_code == 200
        assert resp.json() == []

    def test_returns_all_members_for_project(self, seeded_client):
        """After adding two members, GET returns both in the response list."""
        pid = seeded_client.project_id
        _add_member(seeded_client, pid, name="Alice")
        _add_member(seeded_client, pid, name="Bob")

        resp = seeded_client.get(f"/projects/{pid}/crew")
        assert resp.status_code == 200
        names = [m["name"] for m in resp.json()]
        assert "Alice" in names
        assert "Bob" in names

    def test_does_not_leak_other_project_members(self, seeded_client, SessionFactory):
        """Members belonging to a different project are not returned."""
        from app.models import CrewMember, Project

        session = SessionFactory()
        try:
            # Create a second independent project and a crew member for it
            other = Project(
                name="Other Project",
                location_city="Haifa",
                latitude=32.8,
                longitude=35.0,
            )
            session.add(other)
            session.flush()
            session.add(CrewMember(project_id=other.id, name="Stranger"))
            session.commit()
        finally:
            session.close()

        resp = seeded_client.get(f"/projects/{seeded_client.project_id}/crew")
        assert resp.status_code == 200
        names = [m["name"] for m in resp.json()]
        assert "Stranger" not in names


